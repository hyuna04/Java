package RV.JTJ;

public class _03_01 {
    public static void main(String[] args) {
        //자료형

        //숫자연산
        int a = 10;
        int b = 5;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        //증감연산(++, --)
        int i = 0;
        int j = 10;
        i++;
        j--;

        System.out.println(i); //1출력
        System.out.println(j); //9출력
    }

}

